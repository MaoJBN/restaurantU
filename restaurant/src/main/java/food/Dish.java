
package food;

public class Dish {
    String type;
    int quanty;
    float price;
    String nameDish;
    String ingredients;

    public Dish(String type, int quanty, float price, String nameDish, String ingredients) {
        this.type = type;
        this.quanty = quanty;
        this.price = price;
        this.nameDish = nameDish;
        this.ingredients = ingredients;
    }
    
}
